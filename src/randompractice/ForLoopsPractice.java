package randompractice;

public class ForLoopsPractice {
    public static void main(String[] args) {
        System.out.println("I love Cat");// print million opf lines in one line fo code
        for(int i= 0;i<10; i++){
            System.out.println("I love cat");
        }
        for(int i=0; i<15; i++){
            System.out.println(i);
        }

        int[] grades ={ 98, 100, 83, 90, 93,};

        for(int i= 0; i<grades.length;i ++){
            System.out.println(grades[i]);
        }
    }

}
